// Result Screen
//
// Displays the verification result (success or failure).

import 'package:flutter/material.dart';

/// Displays the outcome of an age verification attempt.
///
/// Shows a success or failure state with contextual information about what
/// happened during the zero knowledge proof flow.
class ResultScreen extends StatelessWidget {
  /// Whether the age verification succeeded.
  final bool verified;

  /// The age threshold that was being verified.
  final int minimumAge;

  /// An optional error message to display when [verified] is false.
  final String? errorMessage;

  /// Creates a [ResultScreen].
  const ResultScreen({
    super.key,
    required this.verified,
    required this.minimumAge,
    this.errorMessage,
  });

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text('Result'),
        automaticallyImplyLeading: false,
        backgroundColor: Theme.of(context).colorScheme.primaryContainer,
      ),
      body: SafeArea(
        child: Padding(
          padding: const EdgeInsets.all(20),
          child: Column(
            children: [
              const Spacer(),

              // Result icon
              Icon(
                verified ? Icons.check_circle : Icons.cancel,
                size: 80,
                color: verified ? Colors.green : Colors.red,
              ),

              const SizedBox(height: 24),

              Text(
                verified ? 'Age Verified!' : 'Verification Failed',
                style: Theme.of(context).textTheme.headlineSmall?.copyWith(
                      fontWeight: FontWeight.bold,
                      color: verified ? Colors.green : Colors.red,
                    ),
              ),

              const SizedBox(height: 12),

              Text(
                verified
                    ? 'The user has successfully proven they are $minimumAge years or older.'
                    : errorMessage ??
                        'Unable to verify the user is $minimumAge years or older.',
                style: Theme.of(context).textTheme.bodyMedium?.copyWith(
                      color: Colors.grey[600],
                    ),
                textAlign: TextAlign.center,
              ),

              const SizedBox(height: 24),

              // Info card
              Card(
                child: Padding(
                  padding: const EdgeInsets.all(16),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Text(
                        verified ? 'What happened:' : 'Possible reasons:',
                        style: const TextStyle(fontWeight: FontWeight.bold),
                      ),
                      const SizedBox(height: 12),
                      if (verified) ...[
                        _buildBulletPoint(
                          'A zero knowledge proof was generated by Provii Wallet',
                        ),
                        _buildBulletPoint(
                          'The proof was verified cryptographically',
                        ),
                        _buildBulletPoint(
                          'The user\'s actual date of birth was never revealed',
                        ),
                        _buildBulletPoint(
                          'The session has been redeemed and cannot be replayed',
                        ),
                      ] else ...[
                        _buildBulletPoint(
                            'The user cancelled the verification'),
                        _buildBulletPoint('The verification challenge expired'),
                        _buildBulletPoint(
                          'The user doesn\'t meet the age requirement',
                        ),
                        _buildBulletPoint(
                            'A network or technical error occurred'),
                      ],
                    ],
                  ),
                ),
              ),

              const SizedBox(height: 16),

              // Access card
              Container(
                padding: const EdgeInsets.all(16),
                decoration: BoxDecoration(
                  color: verified
                      ? Colors.green.withOpacity(0.1)
                      : Colors.red.withOpacity(0.1),
                  borderRadius: BorderRadius.circular(12),
                ),
                child: Row(
                  children: [
                    Icon(
                      verified ? Icons.lock_open : Icons.lock,
                      color: verified ? Colors.green : Colors.red,
                    ),
                    const SizedBox(width: 12),
                    Expanded(
                      child: Text(
                        verified
                            ? 'Access to age-restricted content can now be granted.'
                            : 'Access to age-restricted content should be denied.',
                        style: TextStyle(
                          color: verified ? Colors.green[700] : Colors.red[700],
                        ),
                      ),
                    ),
                  ],
                ),
              ),

              const Spacer(),

              FilledButton(
                onPressed: () {
                  Navigator.pushNamedAndRemoveUntil(
                    context,
                    '/threshold',
                    (route) => false,
                  );
                },
                style: FilledButton.styleFrom(
                  minimumSize: const Size(double.infinity, 50),
                ),
                child: Text(verified ? 'Start Over' : 'Try Again'),
              ),
            ],
          ),
        ),
      ),
    );
  }

  Widget _buildBulletPoint(String text) {
    return Padding(
      padding: const EdgeInsets.only(bottom: 8),
      child: Row(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text('• ', style: TextStyle(color: Colors.grey[600])),
          Expanded(
            child: Text(
              text,
              style: TextStyle(color: Colors.grey[600], fontSize: 14),
            ),
          ),
        ],
      ),
    );
  }
}
